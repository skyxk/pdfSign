package com.clt.ess.entity;

public class SignatureLog {

    private String signatureLogId;
    private String signSerialNum;
    private String signTime;

    public String getSignatureLogId() {
        return signatureLogId;
    }

    public void setSignatureLogId(String signatureLogId) {
        this.signatureLogId = signatureLogId;
    }

    public String getSignSerialNum() {
        return signSerialNum;
    }

    public void setSignSerialNum(String signSerialNum) {
        this.signSerialNum = signSerialNum;
    }

    public String getSignTime() {
        return signTime;
    }

    public void setSignTime(String signTime) {
        this.signTime = signTime;
    }
}
