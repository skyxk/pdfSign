package com.clt.ess.entity;

public class BusinessSys {

    private String businessSysId;
    private String ftpAccount;
    private String ftpPsw;
    private String ftpPath;

    public String getBusinessSysId() {
        return businessSysId;
    }

    public void setBusinessSysId(String businessSysId) {
        this.businessSysId = businessSysId;
    }

    public String getFtpAccount() {
        return ftpAccount;
    }

    public void setFtpAccount(String ftpAccount) {
        this.ftpAccount = ftpAccount;
    }

    public String getFtpPsw() {
        return ftpPsw;
    }

    public void setFtpPsw(String ftpPsw) {
        this.ftpPsw = ftpPsw;
    }

    public String getFtpPath() {
        return ftpPath;
    }

    public void setFtpPath(String ftpPath) {
        this.ftpPath = ftpPath;
    }
}
